  SymmSpace::Info info;
  info.centrosymmetric = this->CentroSymmetric;
  info.matrices = this->Matrices;
  info.matrices.AddNew().I();
  if (this->CentroSymmetric)
    info.inv_trans = this->InversionCenter;
