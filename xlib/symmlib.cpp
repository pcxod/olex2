size_t TSymmLib::FindPointGroupGroups(const TSpaceGroup& PointGroup,
  TPtrList<TSpaceGroup>& res) const
{
  size_t rc = res.Count();
  for( size_t i=0; i < SGCount(); i++ )  {
    if( &GetGroup(i).GetPointGroup() == &PointGroup )
      res.Add(GetGroup(i));
  }
  return res.Count()-rc;
}
