size_t TSymmLib::FindLaueClassGroups(const TSpaceGroup& LaueClass,
  TPtrList<TSpaceGroup>& res) const
{
  size_t rc = res.Count();
  for( size_t i=0; i < SGCount(); i++ )  {
    if( &GetGroup(i).GetLaueClass() == &LaueClass )  {
      res.Add(GetGroup(i));
    }
  }
  return res.Count()-rc;
}
